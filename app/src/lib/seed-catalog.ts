import type { GrowthSeed, FateDie } from '@/types/growth';

/**
 * Canonical seed catalog — 48 seeds parsed from Character Creation Examples.csv
 * Seeds are the starting template for entity creation. Each defines base stats,
 * traits, skills, and a KRMA Value (KV) that represents creation cost.
 */

function fateDie(n: number): FateDie {
  const map: Record<number, FateDie> = { 4: 'd4', 6: 'd6', 8: 'd8', 12: 'd12', 20: 'd20' };
  return map[n] || 'd6';
}

function parseList(s: string): string[] {
  if (!s || s === 'None') return [];
  return s.split(',').map(x => x.trim()).filter(Boolean);
}

